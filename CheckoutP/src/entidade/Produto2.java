public class Produto2 {
    
    private int codigo = 0;
    private int valor = 0;

    public Produto2() {
    }
    
    public Produto2(int codigo, int valor){
        this.codigo = codigo;
        this.valor = valor;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
    
}
